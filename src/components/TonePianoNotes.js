import * as Tone from 'tone';

export function playC4() {
    const C4Note = new Tone.Player('https://raw.githubusercontent.com/yna-arpon/Music4Kids/main/src/components/24-piano-keys/C4_trim.mp3').toDestination();
    Tone.loaded().then(() => {
        C4Note.start()
    })
}

